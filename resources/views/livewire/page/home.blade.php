                                <br>
                                Itu sebabnya Bu, selama kehamilan perlu mengonsumsi asupan gizi seimbang dari makanan
                                pokok, lauk pauk khususnya sumber protein hewani yang mengandung cukup karbohidrat,
                                protein, lemak, vitamin, mineral. Penelitian menunjukkan mengkonsumsi susu >= 300
                                ml/hari dapat membantu mencegah kejadian stunting .<br>
